function generateSingleDimension(s) {
  const { key, score } = s
  const label = nm(s)
  const level = score >= 85 ? '卓越' : score >= 70 ? '优秀' : score >= 55 ? '良好' : score >= 40 ? '发展中' : '待提升'

  const value = getDimensionValue(key, level)
  return `### ${label}：${score} 分 · ${level}

**它的价值**

${value}`
}

